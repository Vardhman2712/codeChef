class Codechef {
    public static void main(String[] args) throws java.lang.Exception {
        Scanner sc = new Scanner(System.in);

        int x = sc.nextInt();

        if (x >= 750) {
            System.out.println("YES");
        } else {
            System.out.println("NO");
        }

        sc.close(); // Close the scanner to avoid resource leak
    }
}
